// Go program to illustrate the use of simple for loop
package main

import "fmt"

// Main function
func main() {

	// for loop
	// This loop starts when i = 0
	// executes till i<4 condition is true
	// post statement is i++
	for i := 0; i < 4; i++ { // i will start from 0 and increment until it is less than 4
		fmt.Printf("Print_loop\n") //The print statememt.
	}

}
